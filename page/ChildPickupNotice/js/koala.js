

  function playClickSound() {
  const sounds = ["sound/koala.mp3", "sound/1.kl/ben.mp3"];
  const audio = document.getElementById("sound0");
  let delay = 200;
  sounds.forEach(sound => {
    setTimeout(() => {
    audio.src = sound;
    audio.play();
    }, delay);
    delay += 2500;
  });
  }

