
  function playClickSound25() {
  const sounds = ["sound/panda.mp3", "sound/2.pd/bap.mp3"];
  const audio = document.getElementById("sound0");

  let delay = 200; // Delay 1 giây giữa các âm thanh
  sounds.forEach(sound => {
    setTimeout(() => {
    audio.src = sound;
    audio.play();
    }, delay);
    delay += 2500;
  });
  }

