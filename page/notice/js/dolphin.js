function playClickSound100() {
const sounds = ["sound/dolphin.mp3", "sound/3.dp/adele.mp3"];
const audio = document.getElementById("sound0");

let delay = 200;
sounds.forEach(sound => {
setTimeout(() => {
audio.src = sound;
audio.play();
}, delay);
delay += 3000;
});
}

