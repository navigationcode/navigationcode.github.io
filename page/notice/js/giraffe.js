function playClickSound150() {
const sounds = ["sound/giraffe.mp3", "sound/4.gr/coca.mp3"];
const audio = document.getElementById("sound0");

let delay = 200; // Delay 1 giây giữa các âm thanh
sounds.forEach(sound => {
setTimeout(() => {
audio.src = sound;
audio.play();
}, delay);
delay += 4000;
});
}

